// ============================================================================
// GROUP 2: PresetSimulatorHarness creates valid simulator (1 per fixture)
// Verifies the full path: JSON -> PresetLoadResult -> PistonEngineSimulator
// ============================================================================

class PresetCreationTest : public ::testing::Test,
                           public ::testing::WithParamInterface<std::string> {
protected:
    void SetUp() override {
        harness_ = std::make_unique<PresetSimulatorHarness>();
    }

    void TearDown() override {
        harness_->cleanup();
        harness_.reset();
    }

    std::unique_ptr<PresetSimulatorHarness> harness_;
};

TEST_P(PresetCreationTest, PresetCreatesValidSimulator) {
    std::string fixturePath = GetParam();

    // Act: Load preset and create simulator
    ASSERT_TRUE(harness_->initialize(fixturePath))
        << "Failed to initialize from " << fixturePath
        << ": " << harness_->getLastError();

    // Assert: Engine must be initialized via the Simulator interface
    Simulator* sim = harness_->getSimulator();
    ASSERT_NE(sim, nullptr);

    Engine* engine = sim->getEngine();
    ASSERT_NE(engine, nullptr)
        << fixturePath << ": Engine is null after loadSimulation";

    // Assert: Vehicle must be initialized
    EXPECT_NE(sim->getVehicle(), nullptr)
        << fixturePath << ": Vehicle is null after loadSimulation";

    // Assert: Transmission must be initialized
    EXPECT_NE(sim->getTransmission(), nullptr)
        << fixturePath << ": Transmission is null after loadSimulation";

    // Assert: Engine name is not empty
    EXPECT_FALSE(engine->getName().empty())
        << fixturePath << ": Engine name is empty";

    // Assert: Engine has cylinders
    EXPECT_GT(engine->getCylinderCount(), 0)
        << fixturePath << ": Engine has zero cylinders";

    // Assert: Engine displacement is reasonable (>0, <20L)
    engine->calculateDisplacement();
    double displacement = engine->getDisplacement();
    EXPECT_GT(displacement, 0.0)
        << fixturePath << ": Engine displacement must be positive";
    EXPECT_LT(displacement, 20.0e-3)
        << fixturePath << ": Engine displacement unreasonably large";
}

INSTANTIATE_TEST_SUITE_P(
    EnginePresets,
    PresetCreationTest,
    ::testing::Values(FIXTURE_HONDA, FIXTURE_SUBARU)
);

// ============================================================================
// GROUP 3: Non-silent audio at idle (1 per fixture)
//
// Critical test: proves the entire JSON -> physics -> audio pipeline works.
// A preset that produces silence is useless.
// ============================================================================

class PresetAudioIdleTest : public ::testing::Test,
                            public ::testing::WithParamInterface<std::string> {
protected:
    void SetUp() override {
        harness_ = std::make_unique<PresetSimulatorHarness>();
    }

    void TearDown() override {
        harness_->cleanup();
        harness_.reset();
    }

    std::unique_ptr<PresetSimulatorHarness> harness_;
};

TEST_P(PresetAudioIdleTest, ProducesNonSilentAudioAtIdle) {
    std::string fixturePath = GetParam();

    // Arrange: Create and initialize preset simulator
    ASSERT_TRUE(harness_->initialize(fixturePath))
        << fixturePath << ": Failed to initialize preset simulator";

    // Enable engine: ignition + starter
    harness_->setIgnition(true);
    harness_->setStarterMotor(true);

    // Act: Run simulation for enough time for engine to "start" and produce audio
    for (int i = 0; i < 30; ++i) {
        harness_->getSimulator()->startFrame(1.0 / 60.0);
        harness_->getSimulator()->simulateStep();
        harness_->getSimulator()->endFrame();
    }

    // Disable starter once engine is "running"
    harness_->setStarterMotor(false);

    // Capture audio
    auto audio = harness_->runAndCapture(IDLE_FRAMES);
    ASSERT_FALSE(audio.empty())
        << fixturePath << ": No audio output captured";

    // Assert: Audio must contain signal above noise floor
    EXPECT_TRUE(PresetSimulatorHarness::hasSignal(audio))
        << fixturePath << ": Audio output is silent (all samples below noise floor)";

    // Assert: RMS must be above minimum threshold
    float audioRms = PresetSimulatorHarness::rms(audio);
    EXPECT_GT(audioRms, 0.001f)
        << fixturePath << ": Audio RMS too low (" << audioRms << ")";
}

