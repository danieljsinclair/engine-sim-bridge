//   PresetEngineFactory::loadFromFile(path) returns PresetLoadResult
//   containing raw Engine*/Vehicle*/Transmission* pointers.
//   Caller creates a PistonEngineSimulator and calls loadSimulation().
//
// Test count: 15 (7 infrastructure + 4 per-engine * 2 engines)
//
// Data dependency: Tests need JSON preset files in test/fixtures/
// Generated by: engine-sim-preset-compiler <script.mr> <output.json>
//
// Build: cmake -DBUILD_PRESET_ENGINE_TESTS=ON
//   macOS: 15 tests (golden file tests use Piranha as reference)
//   iOS: 15 tests (golden file tests skipped -- no Piranha on iOS)

