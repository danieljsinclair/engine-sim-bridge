namespace {
    constexpr int TEST_SAMPLE_RATE = 48000;
    constexpr int STEREO_CHANNELS = 2;
    constexpr int IDLE_FRAMES = 4800;          // 100ms at 48kHz

    // Noise floor threshold for "non-silent" detection
    constexpr float NOISE_FLOOR = 100.0f;  // int16_t scale

    // Fixture paths - TEST_FIXTURE_DIR is set by CMake to the absolute
    // path of engine-sim-bridge/test/fixtures/
    const std::string FIXTURE_HONDA = TEST_FIXTURE_DIR "/01_honda_trx520.preset.json";
    const std::string FIXTURE_SUBARU = TEST_FIXTURE_DIR "/01_subaru_ej25_eh.preset.json";
}

// ============================================================================
// Test Harness - Preset simulator lifecycle and audio capture
//
// Creates a PistonEngineSimulator, loads a JSON preset via
// PresetEngineFactory::loadFromFile(), then calls loadSimulation().
// Follows the same pattern as EngineSimLoadPreset() in engine_sim_bridge.cpp.
// ============================================================================

class PresetSimulatorHarness {
public:
    PresetSimulatorHarness()
        : simulator_(nullptr)
        , engine_(nullptr)
        , vehicle_(nullptr)
        , transmission_(nullptr)
    {}

    ~PresetSimulatorHarness() {
        cleanup();
    }

    // Load a JSON preset file and wire it into a PistonEngineSimulator.
    // Returns true on success.
    bool initialize(const std::string& fixturePath) {
        // Step 1: Load preset from JSON
        PresetLoadResult result = PresetEngineFactory::loadFromFile(fixturePath);
        if (!result.success()) {
            lastError_ = result.error;
            return false;
        }

        engine_ = result.engine;
        vehicle_ = result.vehicle;
        transmission_ = result.transmission;
        presetName_ = result.presetName;

        // Step 2: Create and initialize PistonEngineSimulator
        auto* pistonSim = new PistonEngineSimulator();
        pistonSim->setFluidSimulationSteps(8);

        Simulator::Parameters params;
        params.systemType = Simulator::SystemType::NsvOptimized;
        pistonSim->initialize(params);
        pistonSim->setSimulationFrequency(10000);
        pistonSim->setTargetSynthesizerLatency(0.05);

        simulator_ = pistonSim;

        // Step 3: Load the engine into the simulator
        simulator_->loadSimulation(engine_, vehicle_, transmission_);

        return true;
    }

    // Run simulation and capture audio output (on-demand rendering)
    std::vector<int16_t> runAndCapture(int framesToGenerate) {
        if (!simulator_) return {};

        std::vector<int16_t> audioOutput(framesToGenerate * STEREO_CHANNELS, 0);

        const int framesPerStep = 100;
        int framesGenerated = 0;

        while (framesGenerated < framesToGenerate) {
            int framesToRead = std::min(framesPerStep, framesToGenerate - framesGenerated);

            simulator_->startFrame(1.0 / 60.0);
            simulator_->simulateStep();
            simulator_->endFrame();

            int framesRead = simulator_->readAudioOutput(
                framesToRead,
                audioOutput.data() + framesGenerated * STEREO_CHANNELS
            );

            if (framesRead <= 0) {
                simulator_->synthesizer().renderAudioOnDemand();
                framesRead = simulator_->readAudioOutput(
                    framesToRead,
                    audioOutput.data() + framesGenerated * STEREO_CHANNELS
                );
            }

            if (framesRead <= 0) break;

            framesGenerated += framesRead;
        }

        return audioOutput;
    }

    // Calculate RMS of audio samples
    static float rms(const std::vector<int16_t>& samples) {
        if (samples.empty()) return 0.0f;
        double sumSquares = 0.0;
        for (int16_t s : samples) {
            double f = static_cast<double>(s) / 32768.0;
            sumSquares += f * f;
        }
        return static_cast<float>(std::sqrt(sumSquares / samples.size()));
    }

    // Check if audio contains signal above noise floor
    static bool hasSignal(const std::vector<int16_t>& samples, int16_t threshold = 100) {
        for (int16_t s : samples) {
            if (std::abs(s) > threshold) return true;
        }
        return false;
    }

    // Engine controls
    void setThrottle(float t) {
        if (simulator_ && simulator_->getEngine()) {
            simulator_->getEngine()->setThrottle(static_cast<double>(t));
        }
    }

    void setIgnition(bool on) {
        if (simulator_ && simulator_->getEngine()) {
            simulator_->getEngine()->setSpeedControl(on ? 1.0 : 0.0);
        }
    }

    void setStarterMotor(bool on) {
        if (simulator_) {
            simulator_->m_starterMotor.m_enabled = on;
        }
    }

    double getRPM() const {
        if (simulator_ && simulator_->getEngine()) {
            return simulator_->getEngine()->getRpm();
        }
        return 0.0;
    }

    Simulator* getSimulator() { return simulator_; }
    Engine* getEngine() { return engine_; }
    Vehicle* getVehicle() { return vehicle_; }
    Transmission* getTransmission() { return transmission_; }
    const std::string& getPresetName() const { return presetName_; }
    const std::string& getLastError() const { return lastError_; }

    void cleanup() {
        if (simulator_) {
            simulator_->destroy();
            delete simulator_;
            simulator_ = nullptr;
        }
        // Note: engine_, vehicle_, transmission_ are owned by the simulator
        // after loadSimulation() is called, so do not delete them separately.
        engine_ = nullptr;
        vehicle_ = nullptr;
        transmission_ = nullptr;
    }

private:
    Simulator* simulator_;
    Engine* engine_;
    Vehicle* vehicle_;
    Transmission* transmission_;
    std::string presetName_;
    std::string lastError_;
    int sampleRate_ = TEST_SAMPLE_RATE;
};

