// ============================================================================
// GROUP 4: Throttle response (1 per fixture)
//
// Verifies that the engine responds to throttle input.
// A non-responding engine is broken or misconfigured.
// ============================================================================

class PresetThrottleTest : public ::testing::Test,
                           public ::testing::WithParamInterface<std::string> {
protected:
    void SetUp() override {
        harness_ = std::make_unique<PresetSimulatorHarness>();
    }

    void TearDown() override {
        harness_->cleanup();
        harness_.reset();
    }

    std::unique_ptr<PresetSimulatorHarness> harness_;
};

TEST_P(PresetThrottleTest, EngineRespondsToThrottleInput) {
    std::string fixturePath = GetParam();

    // Arrange: Create simulator, start engine
    ASSERT_TRUE(harness_->initialize(fixturePath))
        << fixturePath << ": Failed to initialize";

    harness_->setIgnition(true);
    harness_->setStarterMotor(true);

    // Run engine until started
    for (int i = 0; i < 30; ++i) {
        harness_->getSimulator()->startFrame(1.0 / 60.0);
        harness_->getSimulator()->simulateStep();
        harness_->getSimulator()->endFrame();
    }
    harness_->setStarterMotor(false);

    // Act: Set throttle to idle and measure RPM
    harness_->setThrottle(0.0f);
    for (int i = 0; i < 30; ++i) {
        harness_->getSimulator()->startFrame(1.0 / 60.0);
        harness_->getSimulator()->simulateStep();
        harness_->getSimulator()->endFrame();
    }
    double idleRpm = harness_->getRPM();

    // Act: Set throttle to wide-open and measure RPM
    harness_->setThrottle(1.0f);
    for (int i = 0; i < 60; ++i) {
        harness_->getSimulator()->startFrame(1.0 / 60.0);
        harness_->getSimulator()->simulateStep();
        harness_->getSimulator()->endFrame();
    }
    double wotRpm = harness_->getRPM();

    // Assert: WOT RPM must be higher than idle RPM
    EXPECT_GT(wotRpm, idleRpm)
        << fixturePath << ": Engine did not respond to throttle (idle="
        << idleRpm << ", WOT=" << wotRpm << ")";

    // Assert: RPM should be in physically reasonable range
    EXPECT_GT(wotRpm, 500.0)
        << fixturePath << ": WOT RPM unreasonably low (" << wotRpm << ")";
    EXPECT_LT(wotRpm, 12000.0)
        << fixturePath << ": WOT RPM unreasonably high (" << wotRpm << ")";
}

INSTANTIATE_TEST_SUITE_P(
    EnginePresets,
    PresetThrottleTest,
    ::testing::Values(FIXTURE_HONDA, FIXTURE_SUBARU)
);

