TEST_F(PresetInfrastructureTest, DestroyWhileAudioThreadRunning) {
    PresetSimulatorHarness harness;
    ASSERT_TRUE(harness.initialize(FIXTURE_HONDA));

    Simulator* sim = harness.getSimulator();
    sim->startAudioRenderingThread();

    // Give audio thread time to start producing
    std::this_thread::sleep_for(std::chrono::milliseconds(100));

    // Act: Stop audio thread and destroy
    sim->endAudioRenderingThread();
    ASSERT_NO_FATAL_FAILURE(harness.cleanup());

    SUCCEED();
}
